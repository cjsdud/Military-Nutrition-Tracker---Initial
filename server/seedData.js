function monthDates(year, month) {
  const out = [];
  const y = Number(year), m = Number(month);
  const days = new Date(Date.UTC(y, m, 0)).getUTCDate();
  for (let d = 1; d <= days; d++) {
    out.push(`${y}-${String(m).padStart(2, '0')}-${String(d).padStart(2, '0')}`);
  }
  return out;
}

async function ensureFoods(query) {
  const rows = Object.entries(FOODS).map(([name, v]) => ({
    name, calorie: v[0], protein: v[1], carbohydrate: v[2], fat: v[3],
    fiber: v[4], sugar: v[5], sodium: v[6], cholesterol: v[7], saturated_fat: v[8],
  }));
  const res = await query(
    `INSERT INTO foods (name, calorie, protein, carbohydrate, fat, fiber, sugar, sodium, cholesterol, saturated_fat)
     SELECT t.name, t.calorie, t.protein, t.carbohydrate, t.fat, t.fiber, t.sugar, t.sodium, t.cholesterol, t.saturated_fat
     FROM json_to_recordset($1::json) AS t(name text, calorie numeric, protein numeric, carbohydrate numeric,
       fat numeric, fiber numeric, sugar numeric, sodium numeric, cholesterol numeric, saturated_fat numeric)
     ON CONFLICT (name) DO NOTHING`,
    [JSON.stringify(rows)]
  );
  return res.rowCount;
}

async function ensureUnits(query) {
  const rows = UNITS.map(([name, code]) => ({ name, code }));
  const res = await query(
    `INSERT INTO units (name, code)
     SELECT t.name, t.code FROM json_to_recordset($1::json) AS t(name text, code text)
     WHERE NOT EXISTS (SELECT 1 FROM units u WHERE u.name = t.name)`,
    [JSON.stringify(rows)]
  );
  return res.rowCount;
}

// 부대의 식단을 날짜별로 생성. 이미 음식이 들어있는 끼니는 건드리지 않음(보존).
// 부대마다 메뉴 회전을 unitId만큼 어긋나게 해 서로 다른 식단이 되도록 함.
async function generateMealsForUnit(query, unitId, dates) {
  const mealPairs = [];
  const foodRows = [];
  dates.forEach((d, i) => {
    for (const mt of MEAL_TYPES) {
      mealPairs.push({ d, mt });
      const pool = POOLS[mt];
      const menu = pool[(i + Number(unitId)) % pool.length];
      for (const [fname, qty] of menu) foodRows.push({ d, mt, fname, qty });
    }
  });

  await query(
    `INSERT INTO meals (unit_id, meal_date, meal_type)
     SELECT $1, t.d::date, t.mt FROM json_to_recordset($2::json) AS t(d text, mt text)
     ON CONFLICT (unit_id, meal_date, meal_type) DO NOTHING`,
    [unitId, JSON.stringify(mealPairs)]
  );

  const res = await query(
    `INSERT INTO meal_foods (meal_id, food_id, quantity)
     SELECT m.id, f.id, v.qty
     FROM json_to_recordset($2::json) AS v(d text, mt text, fname text, qty int)
     JOIN meals m ON m.unit_id = $1 AND m.meal_date = v.d::date AND m.meal_type = v.mt
     JOIN foods f ON f.name = v.fname
     WHERE NOT EXISTS (SELECT 1 FROM meal_foods x WHERE x.meal_id = m.id)`,
    [unitId, JSON.stringify(foodRows)]
  );
  return res.rowCount;
}

